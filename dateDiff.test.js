const dateDiff = require('./dateDiff').solution;

test('Between 2 Dates',() => {
    expect(dateDiff({'2019-01-01':100,'2019-01-04':115})).toEqual({
        '2019-01-01': 100,
        '2019-01-02': 105,
        '2019-01-03': 110,
        '2019-01-04': 115
    });
});

test('Between 3 Dates',() => {
    expect(dateDiff({'2019-01-10':10,'2019-01-11':20,'2019-01-13':10})).toEqual({
        '2019-01-10': 10,
        '2019-01-11': 20,
        '2019-01-12': 15,
        '2019-01-13': 10
    });
});